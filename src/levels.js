import Tile from './tile.js';

export function buildLevel(game, level) {
    let tiles = [];

    level.forEach((row, rowIndex) => {
        row.forEach((tile, tileIndex) => {
            if (tile !== 0) {
                let position = {
                    x: 50 * tileIndex,
                    y: 50 * rowIndex,
                };
                tiles.push(new Tile(game, position, tile))
            }
            if (tile === 2) {
                let position = {
                    x: 50 * tileIndex,
                    y: 50 * rowIndex,
                };
                tiles.push(new Tile(game, position, tile))
            }
        })
    })
    return tiles;
}

